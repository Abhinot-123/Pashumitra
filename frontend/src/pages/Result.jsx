import { useNavigate } from 'react-router-dom'
import styles from './Result.module.css'

function Result() {
  const navigate = useNavigate()
  const animal = localStorage.getItem('selectedAnimal')
  const language = localStorage.getItem('language')
  const farmerName = localStorage.getItem('farmerName')
  const district = localStorage.getItem('district')
  const result = JSON.parse(localStorage.getItem('result') || '{}')

  function getSeverityColor(severity) {
    if (severity === 'MILD')     return styles.mild
    if (severity === 'MODERATE') return styles.moderate
    if (severity === 'CRITICAL') return styles.critical
  }

  function downloadPDF() {
    const content = `
PASHUMITRA - AI VETERINARY DIAGNOSIS REPORT
============================================
Farmer Name  : ${farmerName}
District     : ${district}
Animal       : ${animal}
Language     : ${language}
Date         : ${new Date().toLocaleDateString()}
Time         : ${new Date().toLocaleTimeString()}

SEVERITY     : ${result.severity}
DIAGNOSIS    : ${result.diagnosis}

SUMMARY
-------
${result.summary}

FIRST AID STEPS
---------------
${(result.firstAid || []).map((s, i) => `${i + 1}. ${s}`).join('\n')}

VET ADVICE
----------
${result.vetAdvice}

============================================
Generated by PashuMitra AI
Powered by Google Gemini
    `
    const blob = new Blob([content], { type: 'text/plain' })
    const url = URL.createObjectURL(blob)
    const a = document.createElement('a')
    a.href = url
    a.download = `PashuMitra_${animal}_${farmerName}.txt`
    a.click()
  }

  return (
    <div className={styles.page}>
      <div className={styles.container}>

        <div className={styles.topBar}>
          <div>
            <h2 className={styles.title}>AI Diagnosis Result</h2>
            <p className={styles.sub}>{animal} · {farmerName} · {district}</p>
          </div>
          <span className={styles.langBadge}>{language}</span>
        </div>

        <div className={`${styles.severityBanner} ${getSeverityColor(result.severity)}`}>
          <span className={styles.severityIcon}>
            {result.severity === 'CRITICAL' ? '🚨' : result.severity === 'MODERATE' ? '⚠️' : '✅'}
          </span>
          <div>
            <div className={styles.severityLabel}>Severity Level</div>
            <div className={styles.severityText}>{result.severity}</div>
          </div>
        </div>

        {result.summary && (
          <div className={styles.card}>
            <h3 className={styles.cardTitle}>📋 Summary</h3>
            <p className={styles.cardText}>{result.summary}</p>
          </div>
        )}

        <div className={styles.card}>
          <h3 className={styles.cardTitle}>🔬 Diagnosis</h3>
          <p className={styles.cardText}>{result.diagnosis}</p>
        </div>

        <div className={styles.card}>
          <h3 className={styles.cardTitle}>🩺 First Aid Steps</h3>
          {(result.firstAid || []).map((step, i) => (
            <div key={i} className={styles.step}>
              <span className={styles.stepNum}>{i + 1}</span>
              <p className={styles.stepText}>{step}</p>
            </div>
          ))}
        </div>

        <div className={styles.card}>
          <h3 className={styles.cardTitle}>👨‍⚕️ Vet Advice</h3>
          <p className={styles.cardText}>{result.vetAdvice}</p>
        </div>

        <div className={styles.btnRow}>
          <button className={styles.downloadBtn} onClick={downloadPDF}>
            📄 Download Report
          </button>
          <button className={styles.alertBtn}>
            🚨 Alert Nearest Vet
          </button>
        </div>

        <button className={styles.homeBtn} onClick={() => navigate('/')}>
          ← Back to Home
        </button>

      </div>
    </div>
  )
}

export default Result